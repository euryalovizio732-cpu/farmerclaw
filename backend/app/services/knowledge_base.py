    """三农知识库访问器"""

    def resolve_best_category(self, product_name: str = "", category: str = "") -> str:
        preferred = self.resolve_canonical_category(product_name)
        if preferred and preferred != product_name:
            return preferred
        fallback = self.resolve_canonical_category(category)
        if fallback:
            return fallback
        return product_name or category

    def resolve_canonical_category(self, text: str) -> str:
        if not text:
            return ""
        for canonical, aliases in CATEGORY_CANONICAL_ALIASES.items():
            if any(alias in text for alias in aliases):
                return canonical
        return text

    def resolve_category_group(self, text: str) -> str:
        canonical = self.resolve_canonical_category(text)
        for group, aliases in CATEGORY_GROUP_ALIASES.items():
            if any(alias in canonical or alias in text for alias in aliases):
                return group
        return "生鲜通用"

    def get_category_keywords(self, category: str) -> dict[str, list[str]]:
        group = self.resolve_category_group(category)
        return CATEGORY_KEYWORDS.get(group, CATEGORY_KEYWORDS.get("生鲜通用", {}))

    def get_category_insights(self, category: str) -> dict[str, Any]:
        canonical = self.resolve_canonical_category(category)
        return CATEGORY_REAL_DATA.get(canonical, {})

    def get_platform_insights(self, platform: str = "douyin") -> dict[str, list[str]]:
        base = PLATFORM_REAL_DATA.get(platform, PLATFORM_REAL_DATA.get("douyin", {}))
        consumer = PLATFORM_REAL_DATA.get("consumer_protection", {})
        ecommerce = PLATFORM_REAL_DATA.get("agri_ecommerce", {})
        return {
            "facts": base.get("facts", []) + consumer.get("facts", []) + ecommerce.get("facts", []),
            "generation_rules": base.get("generation_rules", []) + consumer.get("generation_rules", []) + ecommerce.get("generation_rules", []),
            "sources": base.get("sources", []) + consumer.get("sources", []) + ecommerce.get("sources", []),
        }

    def build_fact_block(self, category: str, platform: str = "douyin") -> str:
        canonical = self.resolve_canonical_category(category)
        group = self.resolve_category_group(category)
        insights = self.get_category_insights(category)
        platform_insights = self.get_platform_insights(platform)
        pricing = self.get_market_pricing(canonical or category)
        lines: list[str] = []

        if canonical:
            lines.append(f"品类定位：{canonical}（归属{group}）")
        if insights.get("origin"):
            lines.append(f"核心产地：{insights['origin']}")
        for fact in insights.get("production_facts", [])[:2]:
            lines.append(f"品类事实：{fact}")
        for fact in insights.get("ecommerce_facts", [])[:2]:
            lines.append(f"电商事实：{fact}")
        if insights.get("topic_angles"):
            lines.append("可用选题角度：" + "；".join(insights["topic_angles"][:3]))
        if insights.get("pain_focus"):
            lines.append("高频风险：" + "；".join(insights["pain_focus"][:3]))
        if insights.get("reply_tips"):
            lines.append("售后回复原则：" + "；".join(insights["reply_tips"][:3]))
        for fact in platform_insights.get("facts", [])[:2]:
            lines.append(f"平台事实：{fact}")
        if platform_insights.get("generation_rules"):
            lines.append("平台写法要求：" + "；".join(platform_insights["generation_rules"][:2]))
        if pricing:
            lines.append(f"价格参考：批发均价{pricing['wholesale_avg']}，电商常见{pricing['ecom_range']}")

        return "\n".join(f"- {line}" for line in lines)

    def get_live_fill_values(self, category: str, product_name: str = "") -> dict[str, str]:
        canonical = self.resolve_best_category(product_name, category)
        insights = self.get_category_insights(canonical)
        traits = insights.get("product_traits", [])
        quality_terms = insights.get("quality_terms", [])
        pricing = self.get_market_pricing(canonical or product_name) or {}
        return {
            "appearance": "、".join(traits[:2]) or "外观匀整",
            "quality": "、".join((quality_terms + traits)[:2]) or "新鲜直发",
            "evaluation": "、".join(traits[:2]) or f"{canonical or product_name}状态在线",
            "savings": "一截" if pricing else "更划算",
            "warranty": "；".join(insights.get("reply_tips", [])[:2]) or "有问题直接走平台处理",
        }

    def get_pain_point_categories(self) -> dict[str, Any]:
        return PAIN_POINT_CATEGORIES

    def get_platform_title_limit(self, platform: str) -> int:
        return PLATFORM_TITLE_LIMITS.get(platform, 60)

    def get_listing_template(self, category: str) -> dict[str, Any]:
        group = self.resolve_category_group(category)
        return LISTING_TEMPLATES.get(group, LISTING_TEMPLATES.get("水果", {}))

    def get_video_framework(self) -> dict[str, list[str]]:
        return VIDEO_SCRIPT_FRAMEWORK

    def classify_pain_point(self, text: str) -> tuple[str, str]:
        """简单关键词分类，返回 (label, opportunity)"""
        text_lower = text.lower()
        for cat_key, cat_data in PAIN_POINT_CATEGORIES.items():
            if any(kw in text_lower for kw in cat_data["keywords"]):
                return cat_data["label"], cat_data["opportunity"]
        return "其他问题", "深入调研用户需求，针对性优化产品与服务"

    def get_all_categories(self) -> list[str]:
        return list(CATEGORY_KEYWORDS.keys())

    def get_season_info(self, month: int) -> dict:
        return SEASON_CALENDAR.get(month, SEASON_CALENDAR[1])

    def get_today_season(self) -> dict:
        from datetime import datetime
        month = datetime.now().month
        return self.get_season_info(month)

    def get_hook_patterns(self) -> list[dict]:
        return DOUYIN_HOOK_PATTERNS

    def get_live_modules(self) -> dict[str, list[dict]]:
        return LIVE_MODULES

    def get_viral_formulas(self) -> dict:
        """获取从真实爆款案例提炼的三套公式"""
        return {
            "opener": FORMULA_OPENER,
            "middle": FORMULA_MIDDLE,
            "closer": FORMULA_CLOSER,
        }

    def get_category_core_words(self, category: str) -> list[str]:
        """获取品类专属高频种草词"""
        canonical = self.resolve_canonical_category(category)
        if canonical in CATEGORY_CORE_WORDS:
            return CATEGORY_CORE_WORDS[canonical]
        if canonical == "脐橙":
            return CATEGORY_CORE_WORDS.get("柑橘专属", CATEGORY_CORE_WORDS.get("水果", []))
        group = self.resolve_category_group(category)
        return CATEGORY_CORE_WORDS.get(group, CATEGORY_CORE_WORDS.get("水果", []))

    def get_market_pricing(self, product: str) -> dict | None:
        """获取真实市场定价参考数据（来源：嘉兴水果市场2024运行报告等）"""
        canonical = self.resolve_canonical_category(product)
        for key, data in MARKET_PRICING_REFERENCE.items():
            if key in canonical or canonical in key or key in product or product in key:
                return data
        return None


